public class Player {

    private int number;
    private int score;


    public Player(int playerNumber){

        number = playerNumber;



    }



    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
